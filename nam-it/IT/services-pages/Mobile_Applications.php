                  <h2 class="classic-title"><span>Wardrobe Manager</span></h2>    
                  <p>NAM has started work on the development of this application</p>
<p>Here are the key features.</p>
<p>The application will enable the user to create, manage, add, delete, schedule, order, donate , discard , set preferences for his/her wardrobe items.</p>
<p>All these functions will GUI based and most of the items can be attached to graphical images.</p>
<p>The Wardrobe manger will also link some RSS feeds for preferred sources with application and provide some alerts for key new arrivals.</p>
<p><strong>Create</strong> - Will allow user to create the wardrobe for the first time.</p>
<p><strong>Manage</strong> - Will allow user to manage their wardrobe moving items around, adding loaners, returning loaners, laundry etc</p>
<p><strong>Add</strong> - For adding new items.</p>
<p><strong>Delete</strong> - For deleting items</p>
<p><strong>Schedule</strong> - To set schedule for weekly, week end, dates, parties, events and suggested wardrobe items. An alert will be sent if item is not in the wardrobe and has been loaned or sent for laundry.</p>
<p><strong>Order</strong> - Will allow user to set up alerts for ordering below a thresh hold level.</p>
<p><strong>Donate</strong> - Will allow user to set up time period for donating or mark items for donations.</p>
<p><strong>Discard</strong> - for discarding the non usable items.</p>
<p><strong>Set Preferences</strong> - Will allow user to set color choices, brand choices, choose RSS feeds, set alerts, thresh hold levels etc.  </p>       
